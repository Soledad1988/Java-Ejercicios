//ciclo do-while - la expresion se evalua al final de la iteracion
public class Ejercicio_39 {
	
	public static void main(String[] args) {
		
		/*do {
			sentencia
		}while(expresion)*/
		
		int num = 1;
		do {
			System.out.println(num);
			num++;
		}while(num<=10);
	}

}
